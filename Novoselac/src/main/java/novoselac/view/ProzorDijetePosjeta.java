    @Override
    public void napuniView() {
         var d = obrada.getEntitet();
        txtIme.setText(d.getIme());
        txtPrezime.setText(d.getPrezime());
        txtOib.setText(d.getOib());
        txtImeRoditelja.setText(d.getImeRoditelja());
        txtTelefonRodtelja.setText(d.getTelefonRoditelja());
    
              
        DefaultListModel<Posjeta> m = new DefaultListModel<>();
        int ukupno=0;
        if(d.getPosjete()!=null && !d.getPosjete().isEmpty()){
            for(Posjeta p : d.getPosjete()){
                ukupno+=p.getDjeca().size();
                m.addElement(p);
            }
        }
        lstPosjeta.setModel(m);
        lstPosjeta.repaint();
//        lblUkupnoDjeceNaPosjeti.setText("Ukupno djece: " + ukupno);
    
   }
 
    @Override
    public void napuniModel() {
       var d = obrada.getEntitet();
       d.setIme(txtIme.getText());
       d.setPrezime(txtPrezime.getText());
       d.setOib(txtOib.getText());
       d.setImeRoditelja(txtImeRoditelja.getText());
       d.setTelefonRoditelja(txtTelefonRodtelja.getText());
   
   }
